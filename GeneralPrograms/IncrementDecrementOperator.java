package generalProgram;

public class IncrementDecrementOperator {
	
	public static void main(String[]args) {
		
		int a=10;
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		
		int b=12;
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);
		
		
	}

}
